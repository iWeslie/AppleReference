 /// Code Voice
 Format a span of text as code by using a backquote (`) before the first character of the span and after the last character of the span. The first and last characters cannot be spaces.
 
 `string`
 
 `<#string#>`
 
 
 /// Emphasis (Italics)
 Add emphasis by using an asterisk (*) or underscore (_) before the first character of the span and after the last character of the span. The first and last characters cannot be spaces. Do not mix asterisks and underscores for the same element.
 
 *string*
 _string_

 *<#string#>*
 _<#string#>_
 
 
 /// Strong (Bold)
 Add strong formatting with two asterisks (**) or two underscores (__) before the first character of the span and after the last character of the span. The first and last characters cannot be spaces. Do not mix asterisks and underscores for the same element.
 
 **string**
 __string__
 
 **<#string#>**
 __<#string#>__
 
 */
